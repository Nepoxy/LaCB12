function getDestinationById(int $id): ?array {
    global $destinations;
    foreach ($destinations as $dest) {
        if ($dest['id'] === $id) return $dest;
    }
    return null;
}

function getDestinationsByCategory(string $category): array {
    global $destinations;
    if ($category === 'all') return $destinations;
    return array_values(array_filter($destinations, fn($d) => $d['category'] === $category));
}

function getFeaturedDestinations(): array {
    global $destinations;
    return array_slice($destinations, 0, 4);
}

function getTopDestinations(): array {
    global $destinations;
    $sorted = $destinations;
    usort($sorted, fn($a, $b) => $b['rating'] <=> $a['rating']);
    return array_slice($sorted, 0, 4);
}
